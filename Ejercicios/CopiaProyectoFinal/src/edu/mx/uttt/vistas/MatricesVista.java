    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        btnSuma.setEnabled(false);
        btnSumaInv.setEnabled(false);
        btnOperaciones.setEnabled(false);
        btnMediana.setEnabled(false);
        btnDesviacion.setEnabled(true);
        btnMedia.setEnabled(false);
        btnModa.setEnabled(false);
        btnDesviacion.setEnabled(false);
        btnImprimir.setEnabled(false);
        txtMediana.setText("");
        txtMedia.setText("");
        txtModa.setText("");
        txtDesviacion.setText("");
        txtSuma.setText("");
        txtSumaInv.setText("");
        DefaultTableModel model = (DefaultTableModel) tblMatriz.getModel(); // Obt√©n el modelo de la tabla
        model.setRowCount(0); // Limpia todas las filas de la tabla
        model.setColumnCount(0);
        if (isNumber(txtFilas.getText()) && isNumber(txtColumnas.getText())) {
            int filas = Integer.parseInt(txtFilas.getText());
            int columnas = Integer.parseInt(txtColumnas.getText());
            operacion = new OperacionesMatrices(filas, columnas);
            btnLlenar.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "Ingresa valores v√°lidos");
        }

    }//GEN-LAST:event_btnCrearActionPerformed

