    public void crearMatriz(int[][] m) {
        DefaultTableModel model = new DefaultTableModel();

        for (int i = 0; i < m[0].length; i++) {
            model.addColumn(String.valueOf("Columna: " + (i + 1))); // Puedes usar i como t√≠tulo de columna o proporcionar tus propios t√≠tulos
        }

        tblMatriz.setModel(model);

        // Agregar filas y datos al modelo
        for (int i = 0; i < m.length; i++) {
            Object[] fila = new Object[m[i].length];

            for (int j = 0; j < m[i].length; j++) {
                fila[j] = m[i][j];
            }

            model.addRow(fila);
        }
    }

