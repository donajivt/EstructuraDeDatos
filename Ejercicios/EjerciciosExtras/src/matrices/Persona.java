public class Persona {
    private String nombre;
    private int edad;
    private String sexo;
    private Sudadera s;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public Sudadera getS() {
        return s;
    }

    public void setS(Sudadera s) {
        this.s = s;
    }
    
}
