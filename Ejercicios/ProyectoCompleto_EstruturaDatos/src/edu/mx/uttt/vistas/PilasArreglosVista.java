    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        if (txtTamanio == null) {
            JOptionPane.showMessageDialog(null, "Introduce el tamaño de la pila, po fis:)");
        } else {
            if (isNumber(txtTamanio.getText())) {
                int n = Integer.parseInt(txtTamanio.getText());
                pa = new PilasArreglos(n);
                btnEncolar.setEnabled(true);
                btnDesen.setEnabled(true);
                jButton2.setEnabled(true);
                btnDesenElem.setEnabled(true);
                btnImprimir.setEnabled(true);
                btnLimpiar.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "Introduce un número entero, po fis:)");
            }
        }
    }//GEN-LAST:event_btnCrearActionPerformed

