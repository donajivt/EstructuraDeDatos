        // TODO add your handling code here:
        String n = JOptionPane.showInputDialog("Ingresa un valor.");
        if (isNumber(n)) {
            int x = Integer.parseInt(n);
            pa.push(x);
        } else {
            JOptionPane.showMessageDialog(null, "Introduce un número entero, po fis:)");
        }
