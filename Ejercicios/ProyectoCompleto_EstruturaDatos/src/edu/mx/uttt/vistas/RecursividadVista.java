    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        if (S1.isSelected()) {
            String x = JOptionPane.showInputDialog("Introduce las veces que se repetirá la serie.");
            if (isNumber(x)) {
                int n = Integer.parseInt(x);
                JOptionPane.showMessageDialog(null, "El resultado es: " +Recursividad.serie1(n));
            }
        } else if (S2.isSelected()) {
            String x = JOptionPane.showInputDialog("Introduce el multiplicando");
            String y = JOptionPane.showInputDialog("Introduce el multiplicador");
            if (isNumber(x) && isNumber(y)) {
                int n = Integer.parseInt(x);
                int m = Integer.parseInt(y);
                Recursividad.serie2(n, m);
            }
        } else if (S3.isSelected()) {
            String x = JOptionPane.showInputDialog("Introduce las veces que se repetirá la serie.");
            if (isNumber(x)) {
                int n = Integer.parseInt(x);
                double v = 3.0;
                double c = 1.0;
                double y = 3.0;
                int i = 1;
                JOptionPane.showMessageDialog(null, "El resultado es: " + Recursividad.serie3(n, y, v, c, i));
            }
        } else if (S4.isSelected()) {
            String x = JOptionPane.showInputDialog("Introduce las veces que se repetirá la serie.");
            if (isNumber(x)) {
                double i=1;
                int n = Integer.parseInt(x);
                JOptionPane.showMessageDialog(null, "Resultado: " + Recursividad.serieDivision2(n,i));
            }
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

